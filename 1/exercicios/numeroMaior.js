var numero1 = parseFloat(prompt("Digite o primeiro numero: "))
var numero2 = parseFloat(prompt("Digite o segundo numero: "))


if (numero1 > numero2) {
    console.log(true)
    console.log("O primeiro numero é maior: " + numero1)
    alert("O primeiro numero é maior: " + numero1)



} else {
    console.log(true)
    console.log("O segundo numero é maior: " + numero2)
    alert("O segundo numero é maior: " + numero2)

}